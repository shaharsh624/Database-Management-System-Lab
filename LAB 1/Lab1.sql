CREATE TABLE CLIENT_MASTER(
	CLIENTNO VARCHAR (6),
	NAME VARCHAR(20),
	ADDRESS1 VARCHAR (30),
	ADDRESS2 VARCHAR (30),
	CITY VARCHAR (15),
	PINCODE INTEGER,
	STATE VARCHAR (15),
	BALDUE DECIMAL (10,2)
);
DESC CLIENT_MASTER;

CREATE TABLE PRODUCT_MASTER(
	PRODUCTNO VARCHAR (6),
	DESCRIPTION VARCHAR(15),
	PROFIT_PERCENT DECIMAL (4,2),
	UNIT_MEASURE VARCHAR (10),
	QTY_ON_HAND INTEGER,
    REORDERL_VL INTEGER,
    SELLPRICE DECIMAL(8,2),
    COST_PRICE DECIMAL(8,2)
);
DESC PRODUCT_MASTER;

CREATE TABLE SALESMAN_MASTER(
	SALESMANNO VARCHAR (6),
	SALESMANNAME VARCHAR(20),
	ADDRESS1 VARCHAR(30),
	ADDRESS2 VARCHAR(30),
	CITY VARCHAR (20),
    PINCODE INTEGER,
    STATE VARCHAR (20),
    SALAMT REAL,
    TGTTOGET DECIMAL,
    YTDSALES DOUBLE(6,2),
    REMARKS VARCHAR (60)
);
DESC SALESMAN_MASTER;


