USE DMBS_LAB;
SHOW TABLES;
CREATE TABLE CLIENT_MASTER_1(
	CLIENTNO VARCHAR (6),
	NAME VARCHAR(20) NOT NULL,
	ADDRESS1 VARCHAR (30),
	ADDRESS2 VARCHAR (30),
	CITY VARCHAR (15),
	PINCODE INTEGER(8),
	STATE VARCHAR (15),
	BALDUE DECIMAL (10,2),
    PRIMARY KEY (CLIENTNO),
    CHECK (CLIENTNO LIKE 'C%')
);
DESC CLIENT_MASTER_1;


CREATE TABLE PRODUCT_MASTER_1(
	PRODUCTNO VARCHAR (6),
	DESCRIPTION VARCHAR(15) NOT NULL,
	PROFIT_PERCENT DECIMAL (4,2) NOT NULL,
	UNIT_MEASURE VARCHAR (10) NOT NULL,
	QTY_ON_HAND INTEGER(8) NOT NULL,
    REORDERL_VL INTEGER(8) NOT NULL,
    SELLPRICE DECIMAL(8,2) NOT NULL,
    COST_PRICE DECIMAL(8,2) NOT NULL,
    PRIMARY KEY (PRODUCTNO),
    CHECK (PRODUCTNO LIKE 'P%')
);
DESC PRODUCT_MASTER_1;


CREATE TABLE SALESMAN_MASTER_1(
	SALESMANNO VARCHAR (6),
	SALESMANNAME VARCHAR(20) NOT NULL,
	ADDRESS1 VARCHAR(30) NOT NULL,
	ADDRESS2 VARCHAR(30),
	CITY VARCHAR (20),
    PINCODE INTEGER(8),
    STATE VARCHAR (20),
    SALAMT REAL(8,2) NOT NULL CHECK(SALAMT!=0),
    TGTTOGET DECIMAL(6,2) NOT NULL CHECK(TGTTOGET!=0),
    YTDSALES DOUBLE(6,2) NOT NULL,
    REMARKS VARCHAR (60),
    PRIMARY KEY (SALESMANNO),
    CHECK (SALESMANNO LIKE 'S%')
);
DESC SALESMAN_MASTER_1;

